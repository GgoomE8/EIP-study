/*
 * [C 연산자 우선순위] (높은 것 → 낮은 것)
 * 1. () [] . ->  (괄호, 배열, 멤버)
 * 2. ! ~ ++ -- + - * & (단항)
 * 3. * / %        (곱·나눗·나머지)
 * 4. + -          (덧·뺄셈)
 * 5. << >>        (시프트)
 * 6. < <= > >=    (비교)
 * 7. == !=        (동등)
 * 8. &            (비트 AND)
 * 9. ^            (비트 XOR)
 *10. |            (비트 OR)
 *11. &&           (논리 AND)
 *12. ||           (논리 OR)
 *13. ? :          (삼항)
 *14. = += -= ...  (대입)
 */
#include <stdio.h>
int main() {
    int x=7, y=4, z;
    z = y%3<3 ? 2 : 1; // 2
    z = z & z >> 1;
    z = x>5 && z<=3 ? z*x : z/x;
    printf("%d", z);
    return 0;
}
